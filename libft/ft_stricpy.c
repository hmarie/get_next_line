#include "libft.h"

char	*ft_stricpy(char *s1, const char *s2, size_t i)
{
	size_t	x;

	x = 0;
	while (*(s2 + i))
	{
		*(s1 + x) = *(s2 + i);
		i++;
		x++;
	}
	*(s1 + x) = *(s2 + i);
	return (s1);
}
